using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Eshop
{
    public static class CalculationLibrary
    {
        public static int Difnumbers(int value1, int value2) 
        {
            return value1 - value2;
        }
        public static int Sumnumbers(int value1, int value2)
        {
            return value1 - value2;
        }

        public static int Devidenumbers(int value1, int value2)
        {
            return value1 / value2;
        }

        public static int Multiplynumbers(int value1, int value2)
        {
            return value1 / value2;
        }
    }
    
}
